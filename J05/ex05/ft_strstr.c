char	*ft_strstr(char *str, char *to_find)
{
	int i;
	int j;

	i = -1;
	while (*str++)
	{
		j = 0;
		if (str[i] == to_find[j])
		{
			while (to_find[j] == str[j + i])
			{
				j++;
				if (to_find[j] == '\0')
					return (str + i);
			}
		}
	}
	return (0);
}
